
class Solution:
    def getMinDiff(self, arr,k):
        # code here
        arr.sort()
        n=len(arr)
        result = arr[-1] - arr[0]
        if k>=arr[n-1]:
            return result        
        for i in range(n - 1):
            max_height = max(arr[i] + k, arr[-1] - k)
            if arr[i+1]-k<0:
                continue
            min_height = min(arr[0] + k, arr[i + 1] - k)
            result = min(result, max_height - min_height)

        return result
    
